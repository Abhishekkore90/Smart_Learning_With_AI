type StudentRemarks = Record<string, string[]>;
type Medium = "marathi" | "semi";

export function CCERemarks({ selectedClass, academicYear, selectedMedium: propMedium, onBack }: { selectedClass: string; academicYear: string; selectedMedium?: string; onBack: () => void }) {
  const [students, setStudents] = useState<Student[]>([]);
  const [loading, setLoading] = useState(true);
  const [saving, setSaving] = useState(false);
  const [activeSemester, setActiveSemester] = useState<Semester>("sem1");
  const [selectedMedium, setSelectedMedium] = useState<Medium>(() => {
    if (propMedium === "semi" || propMedium === "marathi") return propMedium as Medium;
    const stored = localStorage.getItem("cce_selected_medium") || localStorage.getItem("selectedMedium");
    if (stored === "semi" || stored === "marathi") return stored as Medium;
    return "marathi";
  });

  useEffect(() => {
    if (propMedium === "semi" || propMedium === "marathi") {
      setSelectedMedium(propMedium as Medium);
      return;
    }
    const stored = localStorage.getItem("cce_selected_medium") || localStorage.getItem("selectedMedium");
    if (stored === "semi" || stored === "marathi") {
      setSelectedMedium(stored as Medium);
      return;
    }
    async function checkSchoolConfig() {
      try {
        const udise = localStorage.getItem("teacher_udise") || localStorage.getItem("udiseNumber");
        if (udise) {
          const docRef = doc(db, "school_data", `${udise}_class_config`);
          const docSnap = await getDoc(docRef);
          if (docSnap.exists() && docSnap.data().config) {
            const classCfg = docSnap.data().config[selectedClass];
            if (classCfg === "semi") {
              setSelectedMedium("semi");
            } else if (classCfg === "marathi") {
              setSelectedMedium("marathi");
            }
          }
        }
      } catch (err) {
        console.warn("Error checking class config:", err);
      }
    }
    checkSchoolConfig();
  }, [propMedium, selectedClass]);
  const [allRemarks, setAllRemarks] = useState<Record<string, StudentRemarks>>({});
  const [editingStudent, setEditingStudent] = useState<Student | null>(null);
  // Custom Admin Uploaded Remarks state
  const [customClassRemarks, setCustomClassRemarks] = useState<Record<string, string[]>>({});
  // Per-student editor state
  const [studentRemarks, setStudentRemarks] = useState<StudentRemarks>({});
  const [expandedSubject, setExpandedSubject] = useState<string | null>("prathambhasha");
  const [writeText, setWriteText] = useState("");

  // Load class remarks from classRemarksData and fallback to CDN/LocalStorage if present
  useEffect(() => {
    // Reset to clean class & medium specific remarks immediately
    const masterData = getClassRemarks(selectedClass, selectedMedium);
    setCustomClassRemarks(masterData);

    async function fetchCustomAdminRemarks() {
      try {
        const cdnUrl = `https://SGKBRAINOVA.b-cdn.net/cce_remarks/class_${selectedClass}_${selectedMedium}_remarks.json`;
        const res = await fetch(cdnUrl);
        if (res.ok) {
          const data = await res.json();
          if (data && Object.keys(data).length > 0) {
            setCustomClassRemarks(data);
          }
        }
      } catch (err) {
        console.warn("Could not load custom remarks from Bunny CDN:", err);
      }
    }
    fetchCustomAdminRemarks();
  }, [selectedClass, selectedMedium]);

  useEffect(() => {
    const q = query(collection(db, "users"), where("role", "==", "student"), where("class", "==", selectedClass));
    const unsub = onSnapshot(q, (snap) => {
      const data = snap.docs.map(d => ({ id: d.id, ...d.data() })) as Student[];
      setStudents(data.sort((a, b) => parseInt(a.rollNo || "999") - parseInt(b.rollNo || "999")));
    });
    return () => unsub();
  }, [selectedClass]);
  useEffect(() => {
    setLoading(true);
    const ref = doc(db, "cce_remarks_v2", `${selectedClass}_${academicYear}_${activeSemester}`);
    const unsub = onSnapshot(ref, (snap) => {
      setAllRemarks(snap.exists() ? snap.data().records || {} : {});
      setLoading(false);
    }, (err) => {
      console.warn("Remarks fetch error:", err);
      setLoading(false);
    });
    return () => unsub();
  }, [selectedClass, academicYear, activeSemester]);
  const openStudent = async (student: Student) => {
    setEditingStudent(student);
    setStudentRemarks(allRemarks[student.id] || {});
    setExpandedSubject("prathambhasha");
    setWriteText("");
  };
  const removeRemark = (subKey: string, text: string) => {
    setStudentRemarks(prev => ({
      ...prev,
      [subKey]: (prev[subKey] || []).filter(r => r !== text),
    }));
  };
  const addRemark = (subKey: string, text: string) => {
    const trimmed = text.trim();
    if (!trimmed) return;
    setStudentRemarks(prev => ({
      ...prev,
      [subKey]: [...new Set([...(prev[subKey] || []), trimmed])],
    }));
  };
  const saveStudentRemarks = async () => {
    if (!editingStudent) return;
    setSaving(true);
    try {
      const updated = { ...allRemarks, [editingStudent.id]: studentRemarks };
      const ref = doc(db, "cce_remarks_v2", `${selectedClass}_${academicYear}_${activeSemester}`);
      await setDoc(ref, {
        class: selectedClass, academicYear, semester: activeSemester,
        records: updated, updatedAt: new Date().toISOString(),
      }, { merge: true });
      setAllRemarks(updated);
      toast.success("नोंदी जतन झाल्या!.");
      setEditingStudent(null);
    } catch (err: any) {
      toast.error("जतन अयशस्वी:." + err.message);
    }
    setSaving(false);
  };
  // ── STUDENT REMARK EDITOR ──
  if (editingStudent) {
    return (
      <div
        className="text-slate-800 rounded-[2.5rem] border shadow-xl min-h-[600px] flex flex-col relative select-none overflow-hidden bg-white border-slate-200"
        style={{
          fontFamily: "'Inter', 'Noto Sans Devanagari', sans-serif",
        }}
      >
        {/* Header */}
        <div className="flex items-center gap-3 px-5 py-4 flex-shrink-0 border-b border-slate-100">
          <button
            onClick={() => setEditingStudent(null)}
            className="transition-colors cursor-pointer text-slate-850 hover:text-slate-655"
          >
            <ArrowLeft className="size-5" />
          </button>
          <h2 className="text-base font-bold text-slate-800">वर्णनात्मक नोंदी</h2>
        </div>
        {/* Student Information Banner */}
        <div className="flex items-center gap-3 px-5 py-3 flex-shrink-0 border-b border-slate-100 bg-slate-50/50">
          <div className="w-8 h-8 rounded-full font-bold text-sm flex items-center justify-center border border-blue-100 bg-blue-50 text-blue-600 flex-shrink-0">
            {editingStudent.rollNo || students.indexOf(editingStudent) + 1}
          </div>
          <span className="text-[14px] font-semibold text-slate-800 flex-1">
            {editingStudent.fullName || editingStudent.name || "विद्यार्थी."}
          </span>
        </div>
        {/* Scrollable content */}
        <div className="flex-1 overflow-y-auto pb-24 px-4 py-3 space-y-1">
          {GET_REMARK_SUBJECTS(selectedClass).map((sub) => {
            const remarks = studentRemarks[sub.key] || [];
            const isExpanded = expandedSubject === sub.key;
            return (
              <div key={sub.key} className="border-b border-slate-100">
                {/* Subject accordion header */}
                <button
                  onClick={() => { setExpandedSubject(isExpanded ? null : sub.key); setWriteText(""); }}
                  className="w-full flex items-center justify-between py-4 px-1 cursor-pointer"
                >
                  <span
                    className={`text-[15px] font-bold ${isExpanded ? "text-blue-600" : "text-slate-700 hover:text-blue-600"}`}
                  >
                    {sub.label}
                  </span>
                  <div className="flex items-center gap-2">
                    {remarks.length > 0 && (
                      <div
                        className="w-7 h-7 rounded-full flex items-center justify-center bg-blue-50 border border-blue-100"
                      >
                        <span className="text-xs font-bold text-blue-600">
                          {remarks.length}
                        </span>
                      </div>
                    )}
                    {isExpanded
                      ? <ChevronUp className="size-4 text-slate-400" />
                      : <ChevronDown className="size-4 text-slate-400" />
                    }
                  </div>
                </button>
                {/* Expanded content */}
                {isExpanded && (
                  <div className="pb-4 space-y-4">
                    {/* Selected remarks as chips */}
                    {remarks.length > 0 ? (
                      <div className="space-y-2">
                        <label className="text-xs font-black text-slate-400 uppercase tracking-wider block">
                          जोडलेल्या नोंदी (Added Remarks):
                        </label>
                        <div className="flex flex-wrap gap-2">
                          {remarks.map((remark) => (
                            <div
                              key={remark}
                              className="flex items-center gap-1.5 rounded-xl px-3 py-1.5 bg-slate-50 border border-slate-150 text-slate-800 shadow-sm"
                            >
                              <span className="text-xs font-medium text-slate-800">
                                {remark}
                              </span>
                              <button
                                onClick={() => removeRemark(sub.key, remark)}
                                className="transition-colors cursor-pointer flex-shrink-0 text-slate-400 hover:text-slate-655"
                              >
                                <X className="size-3.5" />
                              </button>
                            </div>
                          ))}
                        </div>
                      </div>
                    ) : (
                      <p className="text-xs text-slate-400 font-bold italic">कोणतीही नोंद जोडलेली नाही.</p>
                    )}
                    {/* Class suggestions list if selectedClass has config */}
                    {(selectedClass === "1st" || selectedClass === "2nd" || selectedClass === "3rd" || selectedClass === "4th" || selectedClass === "5th" || selectedClass === "6th" || selectedClass === "7th" || selectedClass === "8th") && (
                      <div className="space-y-2 pt-2 border-t border-dashed border-slate-200">
                        <label className="text-xs font-black text-blue-500 uppercase tracking-wider block">
                          इयत्ता {selectedClass === "1st" ? "१ ली" : selectedClass === "2nd" ? "२ री" : selectedClass === "3rd" ? "३ री" : selectedClass === "4th" ? "४ थी" : selectedClass === "5th" ? "५ वी" : selectedClass === "6th" ? "६ वी" : selectedClass === "7th" ? "७ वी" : "८ वी"} साठी उपलब्ध नोंदी (Click to Select):
                        </label>
                        <div className="max-h-60 overflow-y-auto border border-slate-150 rounded-2xl p-2.5 space-y-1.5 bg-slate-50/30">
                          {((customClassRemarks[sub.key] && customClassRemarks[sub.key].length > 0
                            ? customClassRemarks[sub.key]
                            : getClassRemarks(selectedClass, selectedMedium)[sub.key]) || []
                          ).map((s) => {
                            const adjustedS = adjustRemarkGender(s, editingStudent?.gender);
                            const isSelected = remarks.includes(adjustedS);
                            return (
                              <button
                                key={s}
                                type="button"
                                onClick={() => {
                                  if (isSelected) {
                                    removeRemark(sub.key, adjustedS);
                                  } else {
                                    addRemark(sub.key, adjustedS);
                                  }
                                }}
                                className={`w-full text-left px-3 py-2 rounded-xl text-xs font-medium transition-all cursor-pointer border ${isSelected
                                    ? "bg-blue-50 border-blue-200 text-blue-600 font-bold"
                                    : "bg-white border-slate-150 text-slate-600 hover:bg-slate-50"
                                  }`}
                              >
                                {adjustedS}
                              </button>
                            );
                          })}
                        </div>
                      </div>
                    )}
                    {/* Custom text input always visible */}
                    <div className="space-y-2 pt-2 border-t border-dashed border-slate-200">
                      <label className="text-xs font-black text-slate-600 uppercase tracking-wider block">
                        नवीन नोंद लिहा (Type New Remark):
                      </label>
                      <div className="flex gap-2">
                        <input
                          type="text"
                          value={writeText}
                          onChange={(e) => setWriteText(e.target.value)}
                          onKeyDown={(e) => {
                            if (e.key === "Enter") {
                              e.preventDefault();
                              addRemark(sub.key, writeText);
                              setWriteText("");
                            }
                          }}
                          placeholder="उदा. विद्यार्थ्याची अभ्यासातील प्रगती चांगली आहे."
                          className="flex-1 px-4 py-3 bg-slate-50 border border-slate-200 rounded-xl text-sm font-medium outline-none focus:border-blue-500 focus:bg-white text-slate-800"
                        />
                        <button
                          onClick={() => {
                            addRemark(sub.key, writeText);
                            setWriteText("");
                          }}
                          className="px-6 py-3 font-bold text-sm rounded-xl cursor-pointer transition-colors bg-blue-600 hover:bg-blue-700 text-white shrink-0"
                        >
                          जोडा
                        </button>
                      </div>
                    </div>
                  </div>
                )}
              </div>
            );
          })}
        </div>
        {/* Fixed Save button */}
        <div
          className="absolute bottom-0 left-0 right-0 px-5 pb-5 pt-3 bg-gradient-to-t from-white via-white to-transparent"
        >
          <button
            onClick={saveStudentRemarks}
            disabled={saving}
            className="w-full py-4 bg-blue-600 hover:bg-blue-700 active:scale-[0.99] text-white font-extrabold text-sm rounded-2xl transition-all cursor-pointer shadow-lg disabled:opacity-50"
          >
            {saving ? "जतन होत आहे." : "Save"}
          </button>
        </div>
      </div>
    );
  }
  // ── LIST VIEW ──
  return (
    <div
      className="text-slate-800 rounded-[2.5rem] border shadow-xl min-h-[600px] flex flex-col relative select-none bg-white border-slate-200"
      style={{
        fontFamily: "'Inter', 'Noto Sans Devanagari', sans-serif",
      }}
    >
      {/* Header */}
      <div className="flex items-center gap-4 px-5 py-4 border-b border-slate-100">
        <button
          onClick={onBack}
          className="p-1.5 rounded-full transition-colors cursor-pointer flex items-center justify-center text-slate-850 hover:bg-slate-100"
        >
          <ArrowLeft className="size-5" />
        </button>
        <h2 className="text-lg font-bold tracking-tight text-slate-800">
          वर्णनात्मक नोंदी
        </h2>
      </div>
      {/* Semester Tabs */}
      <div className="px-5 py-3 border-b border-slate-100">
        <div className="flex bg-slate-100 border border-slate-200 rounded-xl p-1">
          {(["sem1", "sem2"] as Semester[]).map((sem) => (
            <button
              key={sem}
              onClick={() => setActiveSemester(sem)}
              className={`flex-1 py-2.5 text-sm font-bold rounded-lg transition-all cursor-pointer ${activeSemester === sem
                  ? "bg-white text-blue-600 shadow-sm border border-slate-200/50"
                  : "text-slate-500 hover:text-slate-800"
                }`}
            >
              {sem === "sem1" ? "प्रथम सत्र." : "द्वितीय सत्र."}
            </button>
          ))}
        </div>
      </div>
      {/* Student List */}
      <div className="flex-1 overflow-y-auto px-4 py-3">
        {loading ? (
          <div className="flex flex-col items-center justify-center py-20 gap-3">
            <div className="animate-spin rounded-full h-8 w-8 border-b-2 border-blue-600" />
            <span className="text-xs font-bold text-slate-400">लोड होत आहे...</span>
          </div>
        ) : students.length === 0 ? (
          <div className="flex justify-center py-20 text-sm text-slate-400">
            विद्यार्थी सापडले नाहीत
          </div>
        ) : (
          <div className="space-y-0.5">
            {students.map((student, idx) => {
              const sr = allRemarks[student.id] || {};
              const totalCount = Object.values(sr).reduce((s, arr) => s + arr.length, 0);
              return (
                <div key={student.id}
                  onClick={() => openStudent(student)}
                  className="flex items-center justify-between px-2 py-3.5 rounded-xl hover:bg-slate-50 transition-colors border border-transparent hover:border-slate-100 cursor-pointer">
                  <div className="flex items-center gap-4">
                    <div className="w-10 h-10 rounded-full bg-blue-50 text-blue-600 border border-blue-100 font-bold text-sm flex items-center justify-center">{idx + 1}</div>
                    <span className="text-[15px] font-medium text-slate-800">{student.fullName || student.name || "विद्यार्थी."}</span>
                  </div>
                  <div
                    className={`w-9 h-9 rounded-full border-2 flex items-center justify-center transition-all ${totalCount > 0
                        ? "border-emerald-500 bg-emerald-500 text-white"
                        : "border-slate-300 bg-transparent"
                      }`}>
                    {totalCount > 0 && (
                      <svg className="w-4 h-4 text-white" fill="none" viewBox="0 0 24 24" stroke="currentColor" strokeWidth={3}>
                        <path strokeLinecap="round" strokeLinejoin="round" d="M5 13l4 4L19 7" />
                      </svg>
                    )}
                  </div>
                </div>
              );
            })}
          </div>
        )}
      </div>
    </div>
  );
}
